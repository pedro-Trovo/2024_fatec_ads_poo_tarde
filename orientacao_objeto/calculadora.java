public class calculadora {
    //tipo de retorno
    //nome
    //lista de parametros
    //corpo
    int somar(int n1, int n2){
        int res = n1 + n2;
        System.out.println(res);
        return res;
    }
    int subtrair(int n1, int n2){
        int res = n1 - n2;
        System.out.println(res);
        return res;
    }
    int multiplicar(int n1, int n2){
        int res = n1 * n2;
        System.out.println(res);
        return res;
    }
    int dividir(int n1, int n2){
        int res = n1 / n2;
        System.out.println(res);
        return res;
    }

}
